<script setup>
import area from '@turf/area'
import { computed, toRefs } from 'vue'
import { useI18n } from 'vue-i18n'
import { useStore } from 'vuex'

import { WGS84 } from '@/utils/coordinates/coordinateSystems.js'
import { reprojectGeoJsonData, transformIntoTurfEquivalent } from '@/utils/geoJsonUtils.js'
import { round } from '@/utils/numberUtils'

const props = defineProps({
    geometry: {
        type: Object,
        required: true,
        validator: (value) => value?.type === 'Polygon',
    },
})
const { geometry } = toRefs(props)

const i18n = useI18n()
const store = useStore()

const projection = computed(() => store.state.position.projection)

const geometryWgs84 = computed(() => {
    if (projection.value === WGS84) {
        return geometry.value
    }
    return reprojectGeoJsonData(geometry.value, WGS84, projection.value)
})

/** @type {ComputedRef<string>} */
const humanReadableArea = computed(() => {
    const calculatedArea = area(transformIntoTurfEquivalent(geometryWgs84.value))
    let result = ''
    if (calculatedArea) {
        result += roundValueIfGreaterThan(calculatedArea, 1000, 1000000)
        if (calculatedArea > 10000) {
            result += ' km'
        } else {
            result += ' m'
        }
    }
    return result
})

function roundValueIfGreaterThan(value, threshold, divider) {
    if (value > threshold) {
        return `${round(value / divider, 2)}`
    }
    return `${round(value, 2)}`
}
</script>

<template>
    <div :title="i18n.t('area')" class="d-flex align-items-center">
        <div class="rectangle"></div>
        <div class="area-information ps-2">
            <span class="align-middle" data-cy="feature-area-information"
                >{{ humanReadableArea }}<sup>2</sup></span
            >
        </div>
    </div>
</template>

